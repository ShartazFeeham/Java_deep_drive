package _02_dataTypeAndNumberSystems;

public class IntegerStoring {
    public static void main(String[] args) {
        Integer x = 10;
        Integer y = 10;

        Integer p = 2000;
        Integer q = 2000;

        System.out.println(x == y);
        System.out.println(p == q);
        System.out.println(p.equals(q));
    }
}
